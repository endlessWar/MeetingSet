package com.rutong.warehouse.domain;

/**
 * @author fy
 * @since 20190207
 */
public class Goods {

  private String guid;
  private String name;
  private String code;



  public String getGuid() {
    return guid;
  }

  public void setGuid(String guid) {
    this.guid = guid;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getCode() {
    return code;
  }

  public void setCode(String code) {
    this.code = code;
  }

}
